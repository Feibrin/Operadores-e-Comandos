#include <iostream>
using namespace std;

int main() {
    int x = 5;
    int y = 15;
    
    if (x > y) {
        cout << "x eh maior que y" << endl;
    }
    if (x < y) {
        cout << "x eh menor que y" << endl;
    }
    
    if (x >= y) {
        cout << "x eh maior ou igual a y" << endl;
    }
    if (x <= y) {
        cout << "x eh menor ou igual a y" << endl;
    }
    if (x == y) {
        cout << "x eh igual a y" << endl;
    }
    if (x != y) {
        cout << "x eh diferente de y" << endl;
    }
    
    return 0;
}
    
    
    
    
    
    